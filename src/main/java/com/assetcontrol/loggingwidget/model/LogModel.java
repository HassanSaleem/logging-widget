package com.assetcontrol.loggingwidget.model;

public class LogModel {
    private String level;
    private Long count;

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public Long getCount() {
        return count;
    }

    public void setCount(Long count) {
        this.count = count;
    }
}
